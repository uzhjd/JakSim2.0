DROP TABLE INBODY;

CREATE TABLE INBODY(
    IN_ID INT(8) AUTO_INCREMENT PRIMARY KEY,
    USER_ID VARCHAR(30) NOT NULL,
    IN_HEIGHT FLOAT NOT NULL,
    IN_WEIGHT FLOAT NOT NULL,
    IN_SCORE FLOAT,
    IN_FAT FLOAT,
    IN_MUSCLE FLOAT,
    IN_C_DT DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY(USER_ID) REFERENCES USER_INFO(USER_ID)
    ON DELETE CASCADE
);

